package org.example.arqteturasdois;

public class Caneta {
        // Atributos
        private String cor;
        private String modelo;
        private String carga;

        // Construtor
        public Caneta(String cor, String modelo, String carga) {
            this.cor = cor;
            this.modelo = modelo;
            this.carga = carga;
        }

        // Getters e Setters
        public String getCor() {
            return cor;
        }

        public void setCor(String cor) {
            this.cor = cor;
        }

        public String getModelo() {
            return modelo;
        }

        public void setModelo(String modelo) {
            this.modelo = modelo;
        }

    public String getCarga() {
        return carga;
    }

    public void setCarga(String carga) {
        this.carga = carga;
    }


    }



